namespace Fewd\Core;


abstract class AModule extends AThing
{
	// Internal storage for name
	protected $_Name = '';


	//------------------------------------------------------------------------------------------------------------------
	// Init
	//------------------------------------------------------------------------------------------------------------------
	public function Init()
	{
		parent::Init();

		$this->Core()->AddModule(get_class($this), $this);
	}


	//------------------------------------------------------------------------------------------------------------------
	// Name
	//------------------------------------------------------------------------------------------------------------------
	public function Name() : string
	{
		if($this->_Name === '')
		{
			$this->_Name = basename(dirname($this->ClassFilename()));
		}

		return $this->_Name;
	}
}